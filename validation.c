#include "fillit.h"

/*
**	Проверка на то что считанная тетримино валидная
*/

int			check_symbols(char *buf)
{
	int		i;

	i = 0;
	if (!buf)
		return (0);
	while (i < 20)
	{
		if ((i + 1) % 5 == 0 && buf[i] != '\n')
			return (0);
		if ((buf[i] != '#' && buf[i] != '.') && ((i + 1) % 5 != 0))
			return (0);
		i++;
	}
	if (buf[i] != '\n' && buf[i] != '\0')
		return (0);
	return (1);
}
